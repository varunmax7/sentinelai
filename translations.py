def get_translation(lang, key, default=None):
    """Get translation for a key in specified language"""
    if lang in TRANSLATIONS and key in TRANSLATIONS[lang]:
        return TRANSLATIONS[lang][key]
    elif key in TRANSLATIONS['en']:
        return TRANSLATIONS['en'][key]  # Fallback to English
    return default or key

def get_available_languages():
    """Return list of available language codes"""
    return list(TRANSLATIONS.keys())

def translate(key, **kwargs):
    """Translation helper function that can be imported by forms"""
    from flask import g
    lang = getattr(g, 'language', 'en')
    translation = get_translation(lang, key)
    
    if kwargs and isinstance(translation, str):
        try:
            return translation.format(**kwargs)
        except:
            return translation
    return translation